let hari = prompt("tulis nama hari");

if (hari == "senin") {
    alert("Hari Senin yang cerah dan ceria, Mulai lagi rutinitas yang terbentang luas.");
} else if (hari == "selasa") {
    alert("Selasa datang dengan semangat yang tinggi, Kerja keras dan pantang menyerah selalu kita wujudkan di sini.");
} else if (hari == "rabu") {
    alert("Rabu yang penuh dengan harapan, Kita berjuang untuk mencapai impian.");
} else if (hari == "kamis") {
    alert("Kamis datang dengan kebahagiaan, Bersama keluarga dan teman kita berkumpul dalam kebersamaan.");
} else if (hari == "jumat") {
    alert("Jumat yang dinanti dengan penuh harapan,Hari istimewa untuk beribadah dan bersimpuh");
} else if (hari == "sabtu") {
    alert("Sabtu yang penuh dengan kegembiraan, Waktu untuk bersantai dan menikmati kebahagiaan.");
} else if (hari == "minggu") {
    alert("Minggu yang membawa damai dan ketenangan, Waktu beristirahat dan mengisi energi kembali menjelang hari esok.");
} else {
    alert("coba tulis kata seperti berikut. senin, selasa, rabu, kamis, jumat, sabtu, minggu");
}